import random
import matplotlib.pyplot as plt

N = 8  # 8-Queens

def generate_individual():                                                      # ì„¸ëŒ€ìƒì„±
    return [random.randint(0, N - 1) for _ in range(N)]

def calculate_fitness(individual):                                              # ì í•©ë„ ê³„ì‚°
    non_attacking = 28  # 8C2
    attacks = 0
    for i in range(N):
        for j in range(i + 1, N):
            if individual[i] == individual[j] or abs(individual[i] - individual[j]) == abs(i - j):
                attacks += 1
    return non_attacking - attacks

def selection(pop):                                                             # ì„ íƒ
    total_fitness = sum(calculate_fitness(ind) for ind in pop)
    pick = random.uniform(0, total_fitness)
    current = 0
    for ind in pop:
        current += calculate_fitness(ind)
        if current >= pick:
            return ind
    return pop[0]

def crossover(p1, p2):                                                          # êµì°¨
    point = random.randint(1, N - 2)
    return p1[:point] + p2[point:]

def mutate(ind, rate=0.05):                                                     # ëŒì—°ë³€ì´
    if random.random() < rate:
        idx = random.randint(0, N - 1)
        ind[idx] = random.randint(0, N - 1)
    return ind

def genetic_algorithm(pop_size=100, max_gen=1000):
    population = [generate_individual() for _ in range(pop_size)]

    for generation in range(max_gen):
        population = sorted(population, key=lambda ind: calculate_fitness(ind), reverse=True)
        if calculate_fitness(population[0]) == 28:
            print(f"ğŸ‰ Solution found at generation {generation}")
            return population[0]

        next_gen = population[:10]  # elitism
        while len(next_gen) < pop_size:
            p1 = selection(population)
            p2 = selection(population)
            child = crossover(p1, p2)
            child = mutate(child)
            next_gen.append(child)

        population = next_gen

    print("âŒ No solution found.")
    return None

# ì‹œê°í™” í•¨ìˆ˜
def draw_board(solution):
    fig, ax = plt.subplots()
    # ë°°ê²½ ê·¸ë¦¬ê¸°
    for x in range(N):
        for y in range(N):
            color = 'white' if (x + y) % 2 == 0 else 'gray'
            ax.add_patch(plt.Rectangle((x, y), 1, 1, facecolor=color))

    # í€¸ ê·¸ë¦¬ê¸°
    for col, row in enumerate(solution):
        ax.text(col + 0.5, row + 0.5, 'â™›', fontsize=30, ha='center', va='center', color='red')

    ax.set_xlim(0, N)
    ax.set_ylim(0, N)
    ax.set_xticks([])
    ax.set_yticks([])
    ax.set_aspect('equal')
    plt.gca().invert_yaxis()  # (0,0)ì„ ì¢Œìƒë‹¨ìœ¼ë¡œ
    plt.title("8-Queens Solution", fontsize=16)
    plt.show()

# ì‹¤í–‰
solution = genetic_algorithm()
if solution:
    print("ğŸ§  Final Solution:", solution)
    draw_board(solution)